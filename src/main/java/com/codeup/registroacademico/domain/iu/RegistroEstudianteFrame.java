    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        try {
        String nombre = txtNombre.getText().trim();
        int edad = Integer.parseInt(txtEdad.getText());
        double nota1 = Double.parseDouble(txtNota1.getText());
        double nota2 = Double.parseDouble(txtNota2.getText());
        double nota3 = Double.parseDouble(txtNota3.getText());

        // ‚úÖ Crear lista de notas
        List<nota> notas = List.of(
                new nota(nota1),
                new nota(nota2),
                new nota(nota3)
        );

        // ‚úÖ Crear estudiante
        var nuevo = new Estudiante(nombre, edad, notas, UUID.randomUUID());

        // ‚úÖ Guardar en el servicio
        registroService.agregarEstudiante(nuevo);

        // ‚úÖ Refrescar la tabla con todos los estudiantes
        actualizarListaEstudiantes();

        JOptionPane.showMessageDialog(this,
                "‚úÖ Estudiante guardado correctamente",
                "√âxito", JOptionPane.INFORMATION_MESSAGE);

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this,
                "‚ùå Error: ingrese valores num√©ricos v√°lidos en edad y notas",
                "Error", JOptionPane.ERROR_MESSAGE);
    }


    }//GEN-LAST:event_guardarActionPerformed

